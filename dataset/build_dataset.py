def apply_header(ws, cols: list, widths: list, fill_color: str = "1A252F"):
    """Write a formatted header row and freeze it."""
    ws.append(cols)
    fill = PatternFill("solid", fgColor=fill_color)
    for ci, w in enumerate(widths, 1):
        c = ws.cell(1, ci)
        c.font      = H_FONT
        c.fill      = fill
        c.alignment = CENTER
        c.border    = BD
        ws.column_dimensions[get_column_letter(ci)].width = w
    ws.row_dimensions[1].height = 30
    ws.freeze_panes = "A2"


def style_data_row(ws, row_idx: int, ncols: int, is_safe: bool,
                   colored_cols: dict = None):
    """
    Apply alternating row background and border to a data row.
    colored_cols: {col_index: hex_color} for cells with custom fill.
    """
    bg = "F0FFF4" if is_safe else "FFF5F5"
    for ci in range(1, ncols + 1):
        c = ws.cell(row_idx, ci)
        c.border = BD
        if colored_cols and ci in colored_cols:
            c.fill = PatternFill("solid", fgColor=colored_cols[ci])
        else:
            c.fill = PatternFill("solid", fgColor=bg)


