def make_wav_placeholder(path: Path, duration: float = 2.0, rate: int = 16000):
    """Write a silent WAV placeholder. Real audio is generated by TTS script."""
    n = int(rate * duration)
    with wave.open(str(path), "w") as wf:
        wf.setnchannels(1)
        wf.setsampwidth(2)
        wf.setframerate(rate)
        wf.writeframes(b"\x00\x00" * n)


